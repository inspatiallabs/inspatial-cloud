export function makeLogo(
  symbol: keyof typeof symbols,
  color: BasicFgColor,
): string {
  return ColorMe.fromOptions(
    content.map((row) =>
      center(
        row.map((count, index) =>
          (index % 2 === 1 ? symbols[symbol] : " ")
            .repeat(
              count,
            )
        ).join(""),
      )
    ).join("\n"),
    {
      color,
    },
  );
}
